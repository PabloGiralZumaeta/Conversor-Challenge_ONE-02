    private void cboTemperaturas(JComboBox cbo) {
        cbo.removeAllItems();

        cbo.addItem("Seleccionar");
        for (Temperaturas temperaturas : Temperaturas.values()) {
            cbo.addItem(temperaturas.name());
        }
    }

